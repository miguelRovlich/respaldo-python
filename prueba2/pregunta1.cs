ES UN CICLO
Se solicita crear una función que determine si una cadena de caracteres es un ciclo de otra.
Se considera que una cadena de caracteres es un ciclo si podemos encontrar el mismo patrón comenzando desde cualquier punto de la cadena, y al llegar al final continuar en el principio.
EJEMPLO 1:
Cadena de texto: abcde
•	Caso 1: abcde – es un ciclo
•	Caso 2: bcdea – es un ciclo
•	Caso 3: cdeab – es un ciclo
•	Caso 4: deabc – es un ciclo
•	Caso 5: eabcd – es un ciclo
•	Caso 6: edcba – no es u
•	Caso 6: edcba – no es un ciclo
•	Caso 7: perro – no es un ciclo

EJEMPLO 2:
Cadena de texto: abca
•	Caso 1: aabc – es un ciclo
•	Caso 2: caab – es un ciclo
•	Caso 3: bcaa – es un ciclo
•	Caso 4: abca – es un ciclo
